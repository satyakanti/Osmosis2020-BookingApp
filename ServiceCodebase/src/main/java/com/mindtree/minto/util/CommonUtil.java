package com.mindtree.minto.util;

public class CommonUtil {

	public static String toUpperCase(String string) {
		if (string != null) {
			return string.toUpperCase();
		}
		return null;
	}
	public static String toLowerCase(String string) {
		if (string != null) {
			return string.toLowerCase();
		}
		return null;
	}
}
